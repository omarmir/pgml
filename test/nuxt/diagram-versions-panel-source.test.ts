import { describe, expect, it } from 'vitest'
import { readSourceFile } from './source-test-utils'

describe('diagram versions panel source', () => {
  it('uses migration change metadata instead of raw SQL text length to enable export actions', () => {
    const file = readSourceFile('app/components/pgml/PgmlDiagramVersionsPanel.vue')

    expect(file).toContain('migrationHasChanges = false')
    expect(file).toContain('type PgmlMigrationSelectionScope = \'combined\' | `step:${number}`')
    expect(file).toContain('type PgmlMigrationFormat = \'sql\' | \'kysely\'')
    expect(file).toContain('type PgmlVersionMigrationArtifact = {')
    expect(file).toContain('migrationSteps = []')
    expect(file).toContain('const activeMigrationFormat: Ref<PgmlMigrationFormat> = ref(\'sql\')')
    expect(file).toContain('const activeMigrationScope: Ref<PgmlMigrationSelectionScope> = ref(\'combined\')')
    expect(file).toContain('const combinedMigrationArtifacts = computed<Record<PgmlMigrationFormat, PgmlVersionMigrationArtifact>>(() => {')
    expect(file).toContain('const getMigrationArtifactForFormat = (format: PgmlMigrationFormat) => {')
    expect(file).toContain('const hasMigrationSql = computed(() => getMigrationArtifactForFormat(\'sql\').hasChanges)')
    expect(file).toContain('const hasMigrationKysely = computed(() => getMigrationArtifactForFormat(\'kysely\').hasChanges)')
    expect(file).toContain('const activeMigrationArtifact = computed(() => {')
    expect(file).toContain('const selectedMigrationScopeLabel = computed(() => {')
    expect(file).toContain('const displayedMigrationWarnings = computed(() => {')
    expect(file).toContain('const versionStatCards = computed(() => {')
    expect(file).toContain('label: \'Locked\'')
    expect(file).toContain('label: \'Design\'')
    expect(file).toContain('label: \'Impl\'')
    expect(file).toContain('v-for="stat in versionStatCards"')
    expect(file).toContain('label="Kysely"')
    expect(file).toContain('activeMigrationFormat = \'kysely\'')
    expect(file).toContain('return activeMigrationArtifact.value.label')
    expect(file).toContain('return activeMigrationArtifact.value.content')
    expect(file).toContain('return activeMigrationArtifact.value.hasChanges')
    expect(file).toContain('return activeMigrationArtifact.value.mimeType')
    expect(file).toContain('data-version-migration-scope="combined"')
    expect(file).toContain('v-for="step in migrationSteps"')
    expect(file).toContain(':data-version-migration-scope="`step:${step.index}`"')
    expect(file).toContain('Combined history sequence')
    expect(file).toContain('Validation issue:')
    expect(file).toContain('No forward ${activeMigrationLabel.toLowerCase()} migration generated yet.')
    expect(file).toContain('compareRelationshipSummary')
    expect(file).toContain('Path: {{ version.lineageLabel }}')
    expect(file).toContain('Branch root: {{ version.branchRootLabel }}')
    expect(file).toContain('Leaf')
    expect(file).toContain('const buildVersionMetricBadges = (version: PgmlVersionPanelItem) => {')
    expect(file).toContain('buildCountLabel(version.childCount, \'branch\', \'branches\')')
    expect(file).toContain('buildCountLabel(version.siblingCount, \'sibling\')')
    expect(file).toContain('buildCountLabel(version.descendantCount, \'descendant\')')
    expect(file).toContain('buildCountLabel(version.ancestorCount, \'ancestor\')')
    expect(file).toContain('appendMetricBadge(badges, `Branch size ${version.branchVersionCount}`)')
    expect(file).toContain('appendMetricBadge(badges, `Branch leaves ${version.branchLeafCount}`)')
    expect(file).toContain('appendMetricBadge(badges, `Branch depth ${version.branchMaxDepth}`)')
    expect(file).toContain('v-for="badge in buildVersionMetricBadges(version)"')
    expect(file).toContain('Latest overall')
    expect(file).toContain('Latest {{ version.role }}')
    expect(file).toContain('const comparePresetButtons = computed(() => {')
    expect(file).toContain('label: \'Workspace base to draft\'')
    expect(file).toContain('label: \'Latest to draft\'')
    expect(file).toContain('Latest impl to draft')
    expect(file).toContain('Latest design to draft')
    expect(file).toContain('v-for="preset in comparePresetButtons"')
    expect(file).toContain(':disabled="previewTargetId === version.id"')
  })
})
